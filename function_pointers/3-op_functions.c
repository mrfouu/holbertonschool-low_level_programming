#include "3-calc.h"

/**
 * op_add - add "a" and "b"
 * @a : first integer
 * @b : second integer
 * Return: the result of the add
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub "a" and "b"
 * @a : first integer
 * @b : second integer
 * Return: the result of the sub
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul "a" and "b"
 * @a : first integer
 * @b : second integer
 * Return: the result of the mul
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div "a" and "b"
 * @a : first integer
 * @b : second integer
 * Return: the result of the div
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod "a" and "b"
 * @a : first integer
 * @b : second integer
 * Return: the result of the mod
 */

int op_mod(int a, int b)
{
	return (a % b);
}
